package entity;

public class Batman {

	private int posX;
	private int posY;
	private String percurso;
	
	public int getPosX() {
		return posX;
	}
	public void setPosX(int posX) {
		this.posX = posX;
	}
	public int getPosY() {
		return posY;
	}
	public void setPosY(int posY) {
		this.posY = posY;
	}
	public String getPercurso() {
		return percurso;
	}
	public void setPercurso(String percurso) {
		this.percurso = percurso;
	}
}
